  String _getKeyName(int keyCode) {
    switch (keyCode) {
      case 0x1B: // VK_ESCAPE
        return 'Escape';
      case 0x09: // VK_TAB
        return 'Tab';
      case 0x73: // VK_F4
        return 'F4';
      case 0x5B: // VK_LWIN
      case 0x5C: // VK_RWIN
        return 'Windows Key';
      default:
        return 'Key $keyCode';
    }
  }

